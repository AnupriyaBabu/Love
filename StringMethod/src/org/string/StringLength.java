package org.string;

public class StringLength {
	public static void main(String[] args) {
		//String1
		String str="Greens Technology";
		int length=str.length();
		System.out.println("Length of str:"+length);
		//String2
		String str1="Selenium Automation Tool";
		int length1=str1.length();
		System.out.println("Length of str1:"+length1);
		//String3
		String str2="Velmurugan";
		int length2=str2.length();
		System.out.println("Length of str2:"+length2);
		//String4
		String str3="j a v a p r o g r a m";
		int length3=str3.length();
		System.out.println("Length of str3:"+length3);
		//String5
		String str4="9095484678";
		int length4=str4.length();
		System.out.println("Length of str4:"+length4);
	
	}
}
